		    <h5 class="card-title">Degustación de vinos</h5>
		    <p class="card-text">Para catar correctamente un vino y sacar el máximo provecho a esta acción, es importante que tengamos en cuenta los siguientes aspectos:</p>
		    <p>1. El vino se cata sin la compañía de alimento alguno. Como mucho algún "colín" tipo rosquilleta lo más neutro posible.</p>

			<p>2. Es importante decantar el vino vertiéndolo antes sobre un contenedor de cristal que le permita airearse.</p>

			<p>3. Girar la copa para que el vino se airee y podamos apreciar mejor sus cualidades aromáticas. Es importante que, si vamos a degustar un vino tinto, abramos la botella al menos una hora antes de hacerlo.</p>

			<p>4. Dejarse provocar por los olores a los que se parece. De esta manera es fácil que reconozcamos notas de frutas maduras como la uva o las frutas rojas; o de la madera de la barrica en la que ha estado madurando). Si detectamos un olor avinagrado, lo mejor es desechar el trago ya que, posiblemente, indicará que se ha estropeado. Igual ocurre cuando el vino tiene un olor similar al del corcho. En este segundo supuesto es probable que el vino se haya contaminado al entrar en contacto con una bacteria presente en el tapón de corcho que cierra la botella.</p>

			<p>5. Observarlo atentamente. La vista del vino nos ofrece pistas sobre su edad y el tipo de crianza al que se ha sometido. Para ello hay que centrarse en el ribete (la parte del vino que se pega al cristal). Suele ser de color madera en los vinos reservas o los vinos crianzas.</p>

			<p>6. Su gusto. Al probar un trago de vino debemos ser capaces de reconocer su textura, su grado de acidez (que se denomina también frescura), su grado alcohólico, dulzor, etc. Una vez pasadas esas primeras impresiones hay que prestar atención al regusto. Un hecho que los profesionales denominan el recuerdo. En este momento descubriremos nuevas sensaciones.</p>

			<p>7. La temperatura. Los vinos tintos deben catarse a temperatura de entre los 15 y 18 grados, los vinos blancos a una temperatura que oscile entre los 8 y los 10 grados. A esa temperatura ambos vinos desarrollan todas sus cualidades. Los rosados, también en frío.</p>

			<p>8. Si vamos a catar varios vinos, entre uno y otro hay que limpiar la boca. Para ello, lo mejor es emplear un trago largo de agua y enjuagarse la boca con ella haciendo gárgaras si es necesario. De esta manera nos aseguraremos que eliminamos los restos del trago anterior. Lo mejor es que, en una cata, organicemos los vinos de menor a mayor gradación.</p>

			<p>Hay que tener en cuenta, antes de comenzar la cata, que estas pruebas no deben desarrollarse en espacios cerrados o húmedos como las bodegas. En estas condiciones nuestro olfato está saturado y no es capaz de apreciar las características del vino que estamos tomando. Lo mejor es optar por espacios abiertos y asépticos, lugares en los que no hay elementos que nos afectan.</p>

